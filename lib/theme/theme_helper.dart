/// Class containing the supported color schemes.
class ColorSchemes {
  static final primaryColorScheme = ColorScheme.light(
    // Primary colors
    primary: Color(0XFF002353),
    primaryContainer: Color(0XFF727272),
    secondaryContainer: Color(0X7F002353),

    // Error colors
    errorContainer: Color(0XFF2D223B),
    onErrorContainer: Color(0XFFD7DEDD),

    // On colors(text colors)
    onPrimary: Color(0XFF40D194),
    onPrimaryContainer: Color(0XFF061637),
  );
}

/// Class containing custom colors for a primary theme.
