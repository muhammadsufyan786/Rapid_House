  Color get amber300 => Color(0XFFF8CB58);

  // Black
  Color get black900 => Color(0XFF000000);

  // Blue
  Color get blue50 => Color(0XFFECF2FE);
  Color get blue5001 => Color(0XFFE5EEFF);

  // BlueGray
  Color get blueGray40056 => Color(0X56868686);

  // Grayf
  Color get gray4007f => Color(0X7FC5C5C5);

  // Gray
  Color get gray50 => Color(0XFFF6F9FC);
  Color get gray70019 => Color(0X19565656);

  // Orange
  Color get orange200 => Color(0XFFF9C183);

  // Red
  Color get red700 => Color(0XFFE21E1E);

  // Teal
  Color get teal900 => Color(0XFF012354);

  // White
